package sa.gov.sfd.leaveapproval.core;

import java.util.Objects;

/**
 * @author abdullahalgarni on 15/04/2020 AD
 * @project leaveSystem
 **/
public class EmployeeNID {

    private final Long Id;

    public EmployeeNID(Long employeeNID) {
        if(String.valueOf(employeeNID).length()!=10) {
            throw new IllegalArgumentException("This is not employee nation id number");
        }

        this.Id = employeeNID;
    }

    public Long getId() {
        return Id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        EmployeeNID that = (EmployeeNID) o;
        return Id.equals(that.Id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(Id);
    }


}
